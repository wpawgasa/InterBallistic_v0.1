package dti.internalballistics.cad;

/**
 *
 * @author Administrator
 */
public class InnerCircle {
    
    private double cx;
    private double cy;
    private double radius;
    private double peri;
    private double area;
    private double burntX;

    public InnerCircle() {
    }

    public double getCx() {
        return cx;
    }

    public void setCx(double cx) {
        this.cx = cx;
    }

    public double getCy() {
        return cy;
    }

    public void setCy(double cy) {
        this.cy = cy;
    }

    public double getRadius() {
        return radius;
    }

    public void setRadius(double radius) {
        this.radius = radius;
    }

    public double getPeri() {
        return peri;
    }

    public void setPeri(double peri) {
        this.peri = peri;
    }

    public double getArea() {
        return area;
    }

    public void setArea(double area) {
        this.area = area;
    }

    public double getBurntX() {
        return burntX;
    }

    public void setBurntX(double burntX) {
        this.burntX = burntX;
    }
    
    
    
    
}
