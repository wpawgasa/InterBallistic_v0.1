package dti.internalballistics;

/**
 *
 * @author amabird
 */
public class BurningDistance {

    private Double distance;
    private Double peripheral;
    private Double portArea;

    public BurningDistance() {
    }

    /**
     * @return the distance
     */
    public Double getDistance() {
        return distance;
    }

    /**
     * @param distance the distance to set
     */
    public void setDistance(Double distance) {
        this.distance = distance;
    }

    /**
     * @return the peripheral
     */
    public Double getPeripheral() {
        return peripheral;
    }

    /**
     * @param peripheral the peripheral to set
     */
    public void setPeripheral(Double peripheral) {
        this.peripheral = peripheral;
    }

    /**
     * @return the portArea
     */
    public Double getPortArea() {
        return portArea;
    }

    /**
     * @param portArea the portArea to set
     */
    public void setPortArea(Double portArea) {
        this.portArea = portArea;
    }

}
